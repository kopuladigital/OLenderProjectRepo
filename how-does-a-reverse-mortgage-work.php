                                                        <p>Disadvantages:</p>
                                                        <ul class="list-wrap">
                                                            <li><i class="flaticon-arrow-button"></i>Interest adds up over time, increasing the loan balance.</li>
                                                            <li><i class="flaticon-arrow-button"></i>Home equity decreases as the loan balance grows.</li>
                                                            <li><i class="flaticon-arrow-button"></i>Potential impact on inheritance.</li>
                                                        </ul>
