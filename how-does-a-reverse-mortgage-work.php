                                                <div class="blog__details-inner-content">
                                                    <h4>How the Process Works</h4>
                                                    <div class="about__list-box">
                                                        <ul class="list-wrap">
                                                            <li><i class="flaticon-arrow-button"></i>Application and Counseling: Start with a counseling session with a HUD-approved counselor. Then, submit an application and get your home appraised.</li>
                                                            <li><i class="flaticon-arrow-button"></i>Disbursement Options: You can receive the loan as a lump sum, monthly payments, a line of credit, or a combination of these.</li>
                                                            <li><i class="flaticon-arrow-button"></i>Interest and Fees: Interest accumulates over time, and there are upfront costs and ongoing fees associated with the loan.</li>
                                                        </ul>
                                                    </div>
                                                </div>
                                            </div>
                                        </div>
                                    </div>
                                    <h4>Repayment</h4>
                                    <p>The loan must be repaid when the homeowner dies, sells the home, or no longer lives there as their primary residence. Typically, the home is sold to repay the loan, and any remaining equity goes to the homeowner or their heirs. Heirs can also choose to repay the loan to keep the home.</p>
                                    <h4>Example Calculation</h4>
                                    <p>Consider Jane, a 70-year-old homeowner, with a house valued at $300,000. With a reverse mortgage, she could access around 50-60% of her home’s equity, depending on various factors. If she qualifies for a loan amount of $180,000, this would be her available funds after deducting fees and interest.</p>

