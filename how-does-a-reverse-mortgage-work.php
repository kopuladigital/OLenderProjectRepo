                                                    <div class="about__list-box">
                                                        <h4>Eligibility Requirements</h4>
                                                        <p>To qualify for a reverse mortgage, you must:</p>
                                                        <ul class="list-wrap">
                                                            <li><i class="flaticon-arrow-button"></i>Be at least 62 years old</li>
                                                            <li><i class="flaticon-arrow-button"></i>Use the home as your primary residence</li>
                                                            <li><i class="flaticon-arrow-button"></i>Have significant equity in the home</li>
                                                            <li><i class="flaticon-arrow-button"></i>Undergo a financial assessment to ensure you can cover property taxes, insurance, and maintenance costs</li>
                                                        </ul>
                                                    </div>
                                                </div>
                                            </div>
                                        </div>
                                    </div>
                                    <div class="blog__details-inner">
                                        <div class="row align-items-center">
